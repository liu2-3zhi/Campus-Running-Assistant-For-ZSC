// ============================================================
// IE / 旧版 Edge / 国产双核浏览器检测与拦截脚本
//
// 此脚本在页面 <head> 最顶部同步执行，确保在任何页面内容
// 渲染之前完成检测，将不兼容的浏览器重定向到专用提示页面。
//
// 拦截目标：
//   - IE 10 / IE 11（通过 document.documentMode 检测）
//   - 旧版 Edge（EdgeHTML 引擎，通过 UA 中的 "Edge/数字" 检测）
//   - 国产双核浏览器（360、搜狗、QQ浏览器等）切换到
//     IE/Trident 兼容内核时（UA 同时含有 "Trident/" 和品牌标识）
//
// 注意：IE 6 / 7 / 8 / 9 的拦截通过 HTML 条件注释实现，
//       在此 JS 文件加载之前已由 index.html 中的条件注释处理。
// ============================================================

(function () {
  // 使用立即执行函数表达式（IIFE），避免污染全局命名空间

  // 获取当前浏览器的 User-Agent 字符串，供后续正则匹配使用
  var ua = navigator.userAgent;

  // ----------------------------------------------------------------
  // 检测 1：IE 10 / IE 11
  //
  // Trident 引擎（IE 的排版引擎）会在 document 对象上暴露
  // documentMode 属性，其值为当前文档的 IE 兼容模式版本号。
  // 只有 IE 10 和 IE 11 不支持 HTML 条件注释（条件注释自 IE10
  // 起被废弃），因此需要在此用 JS 单独检测。
  // 示例值：IE10 → 10，IE11 → 11
  // ----------------------------------------------------------------
  if (document.documentMode) {
    // 检测到 IE 10 或 IE 11，跳转到兼容性提示页面
    window.location.href = '/ie.html';
    return; // 立即终止后续检测，避免重复跳转
  }

  // ----------------------------------------------------------------
  // 检测 2：旧版 Edge（EdgeHTML 引擎）
  //
  // 基于 Chromium 的新版 Edge（Chromium Edge / Canary）在 UA 中
  // 使用 "Edg/" 字样（无尾部字母 'e'），而旧版 EdgeHTML 引擎
  // 的 Edge 在 UA 中使用 "Edge/版本号" 格式。
  //
  // 示例 UA 片段（EdgeHTML）：
  //   "Mozilla/5.0 ... Edge/18.17763"
  // 示例 UA 片段（Chromium Edge，不拦截）：
  //   "Mozilla/5.0 ... Edg/114.0.1823.82"
  //
  // 使用 \b 单词边界确保不会误匹配类似 "Edged/xxx" 的字符串。
  // ----------------------------------------------------------------
  if (/\bEdge\/\d+/.test(ua)) {
    // 检测到旧版 EdgeHTML 浏览器，跳转到兼容性提示页面
    window.location.href = '/ie.html';
    return; // 立即终止后续检测
  }

  // ----------------------------------------------------------------
  // 检测 3：国产双核浏览器的 IE/Trident 兼容模式
  //
  // 360浏览器、搜狗浏览器、QQ浏览器等国产浏览器内置了
  // Trident（IE内核）和 WebKit/Blink（极速内核）两套引擎。
  // 在"兼容模式"（Trident内核）下，UA 字符串会同时包含：
  //   1. "Trident/"  —— 表示正在使用 IE 排版引擎
  //   2. 品牌标识符  —— 如 360EE、Sogou、QQBrowser 等
  //
  // 在"极速模式"（WebKit/Blink内核）下，UA 中不含 "Trident/"，
  // 因此此条件不会触发，用户可正常使用应用。
  //
  // 支持检测的品牌标识：
  //   360EE / 360SE  —— 360极速/安全浏览器
  //   QIHU           —— 奇虎（360 Trident 内核的另一标识）
  //   Sogou / MetaSr —— 搜狗浏览器
  //   QQBrowser      —— QQ浏览器
  //   TheWorld       —— 世界之窗浏览器
  //   Maxthon        —— 遨游浏览器
  //   2345Explorer   —— 2345加速浏览器
  // ----------------------------------------------------------------
  if (
    /Trident\//.test(ua) &&
    /360EE|360SE|QIHU|Sogou|MetaSr|QQBrowser|TheWorld|Maxthon|2345Explorer/i.test(ua)
  ) {
    // 检测到国产双核浏览器正在以 IE/Trident 内核运行，跳转到提示页面
    // 提示页面会引导用户切换到极速模式（WebKit/Blink 内核）
    window.location.href = '/ie.html';
    return; // 立即终止后续检测
  }

  // ----------------------------------------------------------------
  // 通过所有检测：浏览器为现代浏览器，继续正常加载页面
  // ----------------------------------------------------------------
}());
